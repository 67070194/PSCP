//Lab 02-09 Character Shifting
#include <stdio.h>

int main() {
    char a, b, c, d, e;

    scanf(" %c", &a);
    scanf(" %c", &b);
    scanf(" %c", &c);
    scanf(" %c", &d);
    scanf(" %c", &e);

    printf("%c\n", a + 1);
    printf("%c\n", b);
    printf("%c\n", c + 1);
    printf("%c\n", d);
    printf("%c\n", e + 1);

    return 0;
}